async def generate_content(template_type: str, context: dict) -> dict:
    generator = GENERATORS.get(template_type)
    
    if not generator:
        return {
            "success": False,
            "error": f"未知的模板类型: {template_type}",
            "available_types": list(GENERATORS.keys()),
        }
    
    try:
        result = generator(context)
        return {
            "success": True,
            "template_type": template_type,
            "data": result,
            "generated_at": datetime.now().isoformat(),
        }
    except Exception as e:
        return {
            "success": False,
            "error": str(e),
            "template_type": template_type,
        }


