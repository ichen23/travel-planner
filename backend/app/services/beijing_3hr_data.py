def format_travel_time(hours):
    if hours < 1:
        minutes = int(hours * 60)
        return f"{minutes}分钟"
    elif hours == int(hours):
        return f"{int(hours)}小时"
    else:
        h = int(hours)
        m = int((hours - h) * 60)
        return f"{h}小时{m}分钟"
