def get_poi_tips(poi_id: str, poi_name: str = ""):
    tips = POI_TIPS.get(poi_id)
    if not tips and poi_name:
        for pid, info in POI_TIPS.items():
            if info["name"] in poi_name or poi_name in info["name"]:
                return info
    return tips
