def generate_expanded_data():
    all_coords = {}
    all_tags = {}
    all_basic_info = {}
    all_tips = {}
    
    for template in CITY_TEMPLATES:
        city = template["city"]
        all_coords[city] = template["coords"]
        all_tags[city] = template["tags"]
        
        all_basic_info[city] = {
            "name": city,
            "province": template["province"],
            "description": template["description"],
            "rating": template["rating"],
            "best_time": template["best_time"],
            "weather_tips": template["weather_tips"],
            "transport": template["transport"],
            "highlights": template["highlights"],
            "price": template["price"],
            "avg_daily_budget": template["avg_daily_budget"],
        }
        
        all_tips[city] = template["tips"]
    
    output = f'''# 扩展城市景点数据库
# 包含热门景点、详细攻略、美食推荐等

EXTENDED_CITY_COORDS = {json.dumps(all_coords, ensure_ascii=False, indent=4)}

EXTENDED_CITY_TAGS = {json.dumps(all_tags, ensure_ascii=False, indent=4)}

EXTENDED_CITY_BASIC_INFO = {json.dumps(all_basic_info, ensure_ascii=False, indent=4)}

EXTENDED_CITY_TIPS = {json.dumps(all_tips, ensure_ascii=False, indent=4)}
'''
    
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        f.write(output)
    
    print(f"✅ 成功生成扩展数据文件: {OUTPUT_FILE}")
    print(f"   共包含 {len(CITY_TEMPLATES)} 个热门城市")
    for city in CITY_TEMPLATES:
        print(f"   - {city['city']} ({city['province']}): {len(city['tips']['attractions'])}个景点, {len(city['tips']['food'])}个美食")

